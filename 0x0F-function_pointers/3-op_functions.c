#include<stdio.h>
#include<stdlib.h>
/**
 * op_add - This is for adding two integers
 * @a: This represents the first integer
 * @b: This represents the second integer
 *
 * Return: result of addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - This is for subtracting two integers
 * @a: This represents the first integer
 * @b: This represents the second integer
 *
 * Return: result of subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - This is for multiplying two integers
 * @a: This is for the first integer
 * @b: This is for the second integer
 *
 * Return: result of multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function for dividing two integers
 * @a: This is for the first integer
 * @b: This is for the second integer
 *
 * Return: result of division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function for finding the remainder of division
 * @a: This is for the first integer
 * @b: This is for second integer
 *
 * Return: remainder of division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
